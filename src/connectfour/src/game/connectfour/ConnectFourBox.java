package game.connectfour;

/**
 * Enum describing the different states of a box in the connect four game.
 * 
 * @author benobiwan
 * 
 */
public enum ConnectFourBox
{
	/**
	 * Empty box.
	 */
	EMPTY,

	/**
	 * Box played by the first player.
	 */
	PLAYER1,

	/**
	 * Box played by the second player.
	 */
	PLAYER2;
}
